package jun12;

public class Loop5 {

    public static void main(String[] args) {
        /*
        7 * 1 = 7
        7 * 2 = 14
        7 * 3 = 21
        7 * 4 = 28
         ...
        7 * 10 = 70
         */

        for(int i = 1; i <= 10; i++ ){

            System.out.println("7 * " + i + " = " + 7*i);
        }



    }
}
