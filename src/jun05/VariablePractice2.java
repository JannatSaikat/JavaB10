package jun05;

public class VariablePractice2 {

    public static void main(String[] args) {
        int a  = 5;
        int b = 12;
        int c = 10;
        int d = 14;

        a = 9;

        System.out.println(c/a);
        System.out.println(b/a);
        System.out.println(d/a);

        System.out.println(c%a);
        System.out.println(b%a);
        System.out.println(d%a);

        System.out.println(a);

        a++;
        a++;
        System.out.println(a);

        d--;
        System.out.println(d);

    }
}
